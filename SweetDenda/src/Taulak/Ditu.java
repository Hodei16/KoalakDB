package Taulak;

public class Ditu {
    private int kodeOsagaia;
    private int kodeSweet;

    public Ditu(int kodeOsagaia, int kodeSweet) {
        this.kodeOsagaia = kodeOsagaia;
        this.kodeSweet = kodeSweet;
    }

	public int getKodeOsagaia() {
		return kodeOsagaia;
	}

	public void setKodeOsagaia(int kodeOsagaia) {
		this.kodeOsagaia = kodeOsagaia;
	}

	public int getKodeSweet() {
		return kodeSweet;
	}

	public void setKodeSweet(int kodeSweet) {
		this.kodeSweet = kodeSweet;
	}
}

